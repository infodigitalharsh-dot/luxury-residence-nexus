export const NavbarButton = ({
  href,
  as: Tag = "button",
  children,
  className,
  variant = "primary",
  ...props
}: {
  href?: string;
  as?: React.ElementType;
  children: React.ReactNode;
  className?: string;
  variant?: "primary" | "secondary" | "luxury";
} & (
  | React.ComponentPropsWithoutRef<"a">
  | React.ComponentPropsWithoutRef<"button">
)) => {
  const baseStyles =
    "px-6 py-2 rounded-full font-medium text-sm relative cursor-pointer transition-all duration-300 inline-block text-center";

  const variantStyles = {
    primary: "bg-accent hover:bg-accent/90 text-black",
    secondary: "bg-transparent border border-white/20 text-white hover:bg-white/10",
    luxury: "bg-accent hover:bg-accent/90 text-black shadow-lg",
  };

  return (
    <Tag
      href={href || undefined}
      className={cn(baseStyles, variantStyles[variant], className)}
      {...props}
    >
      {children}
    </Tag>
  );
};